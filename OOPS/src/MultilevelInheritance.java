
public class MultilevelInheritance  {
public void friedRice()
{
	
	System.out.println("#CHINESE FOOD -FRIEDRICE#");
}
public void noodles()
{
	System.out.println("#CHINESE FOOD -NOODLES#");
}
public static void main(String args[])
{
	MultilevelInheritance m = new MultilevelInheritance();
	m.friedRice();
	m.noodles();
	
}
}
	

